#include "ft_printf.h"

void	ft_putchar(char c, int *i, int *err)
{
	int	e;

	if (*err == -1)
	{
		(*i)++;
		return ;
	}
	e = write(1, &c, 1);
	if (e == -1)
		*err = -1;
	(*i)++;
}
